    // ----- Happy Path #1: 78% charging → 3 readings + format_battery E2E -----

    /// Story 3.3 Happy Path #1. Cited: Story 3.3 TDD contract.
    /// Fixture: in-process mock. Threshold T-1 (Lightweight).
    ///
    /// Mock battery 78% Charging → emits exactly three readings
    /// (BatteryPercent, BatteryState, BatteryPowerRate). Cross-checks the
    /// end-to-end pipeline by formatting the percent+state pair with
    /// `format_battery` (Story 1.3) — must produce `"78% (Charging)"`.
    #[test]
    fn mock_battery_78_charging_yields_three_readings_and_format_battery() {
        let snap = BatterySnapshot {
            index: 0,
            percent: 78.0,
            state: BatterySnapshotState::Charging,
            energy_rate_watts: -12.5, // negative = charging on AC (Boundary #3)
        };
        let readings = readings_from_snapshot(std::slice::from_ref(&snap));
        // Contract: exactly 3 readings for one battery (percent, state, rate).
        assert_eq!(
            readings.len(),
            3,
            "one battery should yield percent + state + rate (3 readings)"
        );

        // BatteryPercent: 78.0, Unit::Percent.
        let pct = readings
            .iter()
            .find(|r| r.kind == MetricKind::BatteryPercent)
            .expect("BatteryPercent reading present");
        assert!((pct.value - 78.0).abs() < f64::EPSILON);
        assert_eq!(pct.unit, Unit::Percent);

        // BatteryState: Charging (ordinal 0.0).
        let st = readings
            .iter()
            .find(|r| r.kind == MetricKind::BatteryState)
            .expect("BatteryState reading present");
        let domain_state = BatteryState::from_value(st.value);
        assert_eq!(
            domain_state,
            BatteryState::Charging,
            "BatteryState value must round-trip to Charging"
        );

        // BatteryPowerRate: -12.5 W (negative = charging per Boundary #3).
        let rate = readings
            .iter()
            .find(|r| r.kind == MetricKind::BatteryPowerRate)
            .expect("BatteryPowerRate reading present");
        assert!((rate.value - (-12.5)).abs() < f64::EPSILON);
        assert_eq!(rate.unit, Unit::Watts);

        // End-to-end cross-check with Story 1.3's format_battery. The percent
        // is 78 (rounded to u8), state is Charging — must produce the canonical
        // PRD §7.1 string. Clamp before the cast: pct is in 0..=100 from a
        // finite filter, so the clamp is a no-op in practice but documents the
        // contract and silences clippy::cast_sign_loss.
        let pct_clamped = pct.value.round().clamp(0.0, 100.0);
        // pct_clamped is finite (T-20) and clamped to 0.0..=100.0, so the cast
        // is safe. Both lints are pedantic about the general f64→u8 truncation.
        #[allow(clippy::cast_possible_truncation, clippy::cast_sign_loss)]
        let pct_u8 = pct_clamped as u8;
        let formatted = format_battery(pct_u8, domain_state);
        assert_eq!(formatted, "78% (Charging)");
    }

