    // ----- Boundary #4: T-20 NaN filter (NaN percent/rate is omitted) -----

    /// Cited: T-20 (finite only). If a driver returns NaN for percent or rate
    /// (some Windows ACPI drivers do this when idle), the NaN reading is
    /// OMITTED — not emitted with a sentinel. The state reading is always
    /// finite (it's an ordinal) and is always emitted.
    #[test]
    fn nan_percent_or_rate_is_omitted_per_t20() {
        let snap = BatterySnapshot {
            index: 0,
            percent: f64::NAN,
            state: BatterySnapshotState::Discharging,
            energy_rate_watts: f64::NAN,
        };
        let readings = readings_from_snapshot(std::slice::from_ref(&snap));
        // No NaN anywhere in the output.
        assert!(
            readings.iter().all(|r| r.value.is_finite()),
            "T-20: no reading value may be NaN/Inf"
        );
        // BatteryState is always finite (ordinal 0..=3); always emitted.
        assert!(
            readings.iter().any(|r| r.kind == MetricKind::BatteryState),
            "BatteryState is always finite and always emitted"
        );
        // Percent + rate were NaN → dropped.
        assert!(readings
            .iter()
            .all(|r| r.kind != MetricKind::BatteryPercent));
        assert!(readings
            .iter()
            .all(|r| r.kind != MetricKind::BatteryPowerRate));
    }

