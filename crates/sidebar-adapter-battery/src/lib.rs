//! `sidebar-adapter-battery` — Battery adapter via starship-battery (Story 3.3).
//!
//! Provides battery percent, state, and power-rate readings via the
//! `starship-battery` crate. Emits `BatteryPercent`, `BatteryState`, and
//! `BatteryPowerRate` per physical battery.
//!
//! ## Architecture
//!
//! The real `starship_battery::Manager` is a concrete type whose `batteries()`
//! method returns an iterator yielding `Result<Battery>`. To make the adapter
//! unit-testable per the TDD contract (Story 3.3 calls for "mock battery 78%
//! charging → 3 readings" tests), we abstract behind a `BatteryBackend` trait.
//! Production code wires the real manager via `RealBatteryBackend`; tests
//! inject `MockBatteryBackend` (auto-generated by `mockall`).
//!
//! The adapter holds a `Mutex<B>` (mirroring Story 3.1's sysinfo adapter) so
//! `read_all` can take `&self` and still refresh. `SensorProvider::read_all`
//! is `&self` (Story 2.1), so interior mutability is required.
//!
//! ## Cited
//!
//! - Story 3.3 TDD contract (Happy Path #1, Boundary #1-#3)
//! - architecture.md §5.1 (Reading/MetricKind/Unit/BatteryState spec), §7.2
//! - nfr-thresholds.md T-1 (Lightweight < 0.1% CPU), T-20 (finite values only)
//! - guardrails.md G15 (Mutex poison recovery)

use std::sync::Mutex;

use sidebar_domain::reading::{finite, MetricKind, Reading, SensorId, Unit};
use sidebar_sensor::descriptor::{CostClass, ProviderTier, SensorDescriptor};
use sidebar_sensor::provider::SensorProvider;

pub mod backend;

use crate::backend::{BatteryBackend, BatterySnapshot, BatterySnapshotState};

/// Metrics emitted by this adapter — declared once so the descriptor is
/// `const`-constructible.
const BATTERY_METRICS: &[MetricKind] = &[
    MetricKind::BatteryPercent,
    MetricKind::BatteryState,
    MetricKind::BatteryPowerRate,
];

/// Descriptor for the battery adapter — `Tier::Basic`, `CostClass::Lightweight`.
///
/// Cited: Story 3.3 Technical Context. Not in the G11 HITL list.
const DESCRIPTOR: SensorDescriptor = SensorDescriptor::new(
    "battery",
    CostClass::Lightweight,
    BATTERY_METRICS,
    ProviderTier::Basic,
);

/// Battery-backed adapter. Holds a `Mutex` around the backend because
/// `refresh_and_snapshot` requires `&mut` and `SensorProvider::read_all` is
/// `&self`.
///
/// Generic over `B: BatteryBackend` so tests can substitute a mock. The
/// production convenience alias [`BatteryAdapter`] fixes `B =
/// RealBatteryBackend`.
pub struct BatteryAdapterGeneric<B: BatteryBackend> {
    backend: Mutex<B>,
}

/// Production adapter wired to the real `starship_battery::Manager`.
///
/// Equivalent to `BatteryAdapterGeneric<RealBatteryBackend>` but with a `new()`
/// constructor that initializes the real backend.
pub type BatteryAdapter = BatteryAdapterGeneric<backend::RealBatteryBackend>;

impl<B: BatteryBackend> BatteryAdapterGeneric<B> {
    /// Construct an adapter wrapping the given backend. The backend is wrapped
    /// in a `Mutex` so `read_all` (which is `&self` per the trait) can still
    /// call `&mut self` refresh methods.
    #[must_use]
    pub fn with_backend(backend: B) -> Self {
        Self {
            backend: Mutex::new(backend),
        }
    }
}

impl BatteryAdapter {
    /// Construct the production adapter backed by a real
    /// `starship_battery::Manager`.
    ///
    /// The first `read_all` call enumerates batteries; there is no eager
    /// refresh here (keeps cold-start cheap, NFR-3).
    #[must_use]
    pub fn new() -> Self {
        Self::with_backend(backend::RealBatteryBackend::new())
    }
}

impl Default for BatteryAdapter {
    fn default() -> Self {
        Self::new()
    }
}

impl<B: BatteryBackend + Send> SensorProvider for BatteryAdapterGeneric<B> {
    fn descriptor(&self) -> &SensorDescriptor {
        &DESCRIPTOR
    }

    fn read_all(&self) -> Vec<Reading> {
        // Lock once per tick. The lock is held for the duration of refresh +
        // snapshot extraction (microseconds on any modern machine). The poller
        // is single-threaded per adapter in v1, so contention is not a concern;
        // the Mutex exists only to satisfy the `&self` signature.
        //
        // G15: recover from poison rather than propagating the panic — the
        // poller's catch_unwind handles the FIRST panic, but the Mutex is left
        // poisoned; this `unwrap_or_else(|e| e.into_inner())` lets subsequent
        // ticks recover rather than tearing down the adapter permanently.
        let mut guard = self
            .backend
            .lock()
            .unwrap_or_else(std::sync::PoisonError::into_inner);
        let snapshots = guard.refresh_and_snapshot();
        readings_from_snapshot(&snapshots)
    }
}

/// Translate a slice of [`BatterySnapshot`]s into the canonical `Vec<Reading>`.
///
/// Pulled out of `read_all` so unit tests can exercise the translation without
/// standing up a backend — this is where the TDD contract's "78% charging → 3
/// readings" assertions live.
///
/// # Finite-value policy (T-20)
///
/// Any reading whose `value` is `NaN` or `±Inf` is OMITTED (not emitted with a
/// sentinel). This matters for `energy_rate_watts` on drivers that return NaN
/// when the battery is idle (some Windows ACPI drivers do this); we drop those
/// readings rather than propagate NaN to the formatter.
///
/// # Sign convention (Boundary #3)
///
/// `energy_rate_watts` is passed through verbatim. `starship-battery` reports
/// positive on most platforms when discharging (battery draining) and negative
/// when charging on AC. The adapter does NOT normalize the sign — the GUI
/// formats it with the sign visible, which is the diagnostic value.
fn readings_from_snapshot(snapshots: &[BatterySnapshot]) -> Vec<Reading> {
    // One battery → up to 3 readings (percent, state, power-rate). The state
    // reading is ALWAYS finite (it's an ordinal 0..=3); percent and rate can
    // be NaN on some drivers, so they go through the T-20 finite filter.
    let mut out = Vec::with_capacity(snapshots.len() * 3);
    for s in snapshots {
        let inst = s.index.to_string();

        // --- BatteryPercent (0..=100, Unit::Percent) ---
        if let Some(pct) = finite(s.percent) {
            out.push(Reading::new(
                SensorId::new("battery", inst.clone()),
                MetricKind::BatteryPercent,
                pct,
                Unit::Percent,
            ));
        }

        // --- BatteryState (ordinal f64, Unit::Count) ---
        // The value is the BatteryState ordinal (Charging=0.0, Discharging=1.0,
        // Idle=2.0, Unknown=3.0) per sidebar_domain::BatteryState::to_value.
        // It is ALWAYS finite (an ordinal 0..=3), so no finite-filter needed.
        // The unit carries no physical dimension (it's a wire-format code); we
        // use Unit::Count as the canonical "unitless counter" per §5.1.
        let state_value = snapshot_state_to_domain(s.state).to_value();
        out.push(Reading::new(
            SensorId::new("battery", inst.clone()),
            MetricKind::BatteryState,
            state_value,
            Unit::Count,
        ));

        // --- BatteryPowerRate (Watts, sign = discharging direction) ---
        // Per Boundary #3: pass through verbatim. Positive = discharging on
        // most platforms, negative = charging on AC. The adapter MUST NOT
        // normalize the sign; NaN is filtered per T-20.
        if let Some(rate) = finite(s.energy_rate_watts) {
            out.push(Reading::new(
                SensorId::new("battery", inst),
                MetricKind::BatteryPowerRate,
                rate,
                Unit::Watts,
            ));
        }
    }
    out
}

/// Map the backend's plain-data state to the domain `BatteryState` ordinal.
///
/// `BatteryState::to_value()` (sidebar-domain) returns the stable f64 wire
/// format (Charging=0.0, Discharging=1.0, Idle=2.0, Unknown=3.0). The
/// `BatteryState` Reading's `value` carries that ordinal.
fn snapshot_state_to_domain(s: BatterySnapshotState) -> sidebar_domain::reading::BatteryState {
    match s {
        BatterySnapshotState::Charging => sidebar_domain::reading::BatteryState::Charging,
        BatterySnapshotState::Discharging => sidebar_domain::reading::BatteryState::Discharging,
        BatterySnapshotState::Idle => sidebar_domain::reading::BatteryState::Idle,
        BatterySnapshotState::Unknown => sidebar_domain::reading::BatteryState::Unknown,
    }
}

